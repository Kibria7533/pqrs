    public static function getTotalMuted($tableName, $division_bbs, $district_bbs)
    {


        try {
            $onJoin = ' ( CAST( k.mouja_jl_code AS UNSIGNED ) = CAST( m.brs_jl_no AS UNSIGNED ))';


            $khatianCount = DB::select("select
            h.district_name as district_name,
            h.district_bbs_code as district_bbs,
            h.system_type,
            sum(h.total) as total_khatian,
            sum(h.drr_count) as drr_count,
            sum(h.elrs_count) as elrs_count,
            sum(h.rsk_count) as rsk_count,
            sum(h.eporcha_count) as eporcha_count
            from (
                     SELECT k.system_type,
                            m.district_name,
                            k.district_bbs_code,
                            k.total,
                            k.drr_count,
                            k.elrs_count,
                            k.rsk_count,
                            k.eporcha_count
                     FROM (
                              SELECT mouja_jl_code,
                                     count(*) AS total,
                                     sum(case when system_type = 'drr' then 1 else 0 end)  as drr_count,
                                     sum(case when system_type = 'rsk' then 1 else 0 end)  as rsk_count,
                                     sum(case when system_type = 'elrs' then 1 else 0 end) as elrs_count,
                                     sum(case when system_type = 'eporcha' then 1 else 0 end) as eporcha_count,
                                     district_bbs_code,
                                     upazila_bbs_code,
                                     system_type
                              FROM $tableName
                              WHERE system_type='eporcha' AND  district_bbs_code=$district_bbs
                              GROUP BY district_bbs_code, upazila_bbs_code, mouja_jl_code
                          ) k
                              INNER JOIN loc_all_moujas m
                                        on ($onJoin)
                     WHERE  m.division_bbs_code = $division_bbs AND k.upazila_bbs_code = m.upazila_bbs_code AND k.district_bbs_code = m.district_bbs_code
                     group by k.district_bbs_code, k.upazila_bbs_code, k.mouja_jl_code
                 ) h group by h.system_type, h.district_bbs_code");

                return !empty($khatianCount[0]->eporcha_count)? (int)$khatianCount[0]->eporcha_count:0;

        }catch (\Exception $e){

            Log::debug('khatians:generate-khatian-count:: Unable to get data for table - '.$tableName.'. Message:'.$e->getMessage());

        }
     }

